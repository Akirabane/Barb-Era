<h1>Bienvenue sur notre jeu en ligne</h1>

<h3 class="c">Synopsis</h3>
<p class="info">Nombreux sont les guerriers et les créatures à vouloir prouver leur valeur sur le champ de bataille.
    Joshua, éreinté de ses nombreuses batailles, décide de rentrer sur sa terre natale. En arrivant sur les lieux,
    il n’y avait plus rien, il ne restait que des ruines ! Joshua s’approcha des décombres de la maison de son grand-père.
    Il se souvint alors que son grand-père gardait une amulette qui avait des pouvoirs surpuissants.
    Assoiffé de vengeance, notre héros part à la recherche du tueur de son grand-père et de l’amulette.
    Malheureusement le voyage de notre Héros, bien que déterminé, est semé d’embûches, à lui de résoudre
    ses différents problèmes qui lui permettront d’atteindre son but.</p>

